//============================================================
// 標頭檔
// cmath : 數學用標頭
//============================================================
#include <iostream>
#include <cmath>
//============================================================
// 命名空間
//============================================================
using namespace std;
//============================================================
// 主程式
//============================================================
int main()
{
//資料A、B
long int iNumberA;
long int iNumberB;

//直接用數學abs回傳
while (cin >> iNumberA >> iNumberB)
    cout << abs(iNumberA - iNumberB) << endl;

return 0;
}